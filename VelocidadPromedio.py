#Programa que calcula la velocidad promedio en un viaje

d=float(input("Dame la distancia recorrida en kilometros: "))
t=float(input("Dame el tiempo que tardo en llegar de una ciudad a otra en horas: "))
v=d/t
print("La velocidad promedio del vehiculo a lo largo del viaje fue: ",v,"km/h")